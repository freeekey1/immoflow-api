package com.dialogflow.object;

public class VenteObject {

	Long id;
	Long nombreVente;
	Long chiffreAffaire;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getNombreVente() {
		return nombreVente;
	}
	public void setNombreVente(Long nombreVente) {
		this.nombreVente = nombreVente;
	}
	public Long getChiffreAffaire() {
		return chiffreAffaire;
	}
	public void setChiffreAffaire(Long chiffreAffaire) {
		this.chiffreAffaire = chiffreAffaire;
	}
	
	
	
}
